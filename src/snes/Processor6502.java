	private void SBC16(int value)
	{
		value=value&0xffff;
		int result;
		if(DECIMAL)
		{
			int A1=A&0xf, A2=A&0xf0, A3=A&0xf00, A4=A&0xf000;
			int V1=value&0xf, V2=value&0xf0, V3=value&0xf00, V4=value&0xf000;
			A1-=V1+(CARRY?0:1);
			A2-=V2;
			A3-=V3;
			A4-=V4;
			if (A1>=0xf)
			{
				A1=(A1+0xa)&0xf;
				A2-=0x10;
			}
			if (A2>=0xf0)
			{
				A2=(A2+0xa0)&0xf0;
				A3-=0x100;
			}
			if (A3>=0xf00)
			{
				A3=(A3+0xa00)&0xf00;
				A4-=0x1000;
			}
			if (A4>=0xf000)
			{
				A4=(A4+0xa000)&0xf000;
				CARRY=false;
			}
			else
				CARRY=true;
			result=A4|A3|A2|A1;
		}
		else
		{
			result=A-value+(CARRY?1:0)-1;
			CARRY=(result>=0);
			OVERFLOW= (((A^value)&(A^(result&0xffff)) & 0x8000)!=0);
		}
		A=(result&0xffff);
		ZERO= ((A&0xffff)==0);
		NEGATIVE= ((A&0x8000)!=0);
	}
