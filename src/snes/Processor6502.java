	//set the P register using the flags
	public void updateP()
	{
		P=0;
		if(CARRY) P|=CARRYBIT;
		if(ZERO) P|=ZEROBIT;
		if(IRQ) P|=IRQBIT;
		if(DECIMAL) P|=DECIMALBIT;
		if(INDEXFLAG) P|=INDEXFLAGBIT;
		if(MEMORYFLAG) P|=MEMORYFLAGBIT;
		if(OVERFLOW) P|=OVERFLOWBIT;
		if(NEGATIVE) P|=NEGATIVEBIT;
		if(EMULATION) P|=EMULATIONBIT;
	}
