	//push to stack
	private void PUSHW16(int word)
	{
		int low=word&0xff;
		int high=(word>>8)&0xff;
		snes.nes.writeByte((S-1)&0xffff, (byte)low);
		snes.nes.writeByte(S, (byte)high);
		S=(S-2)&0xffff;
	}
	private void PUSHW8(int word)
	{
		int low=word&0xff;
		int high=(word>>8)&0xff;
		S=(S&0xff00)|((S-1)&0xff);
		snes.nes.writeByte(S, (byte)low);
		snes.nes.writeByte((S+1)&0xffff, (byte)high);
		S=(S&0xff00)|((S-1)&0xff);
	}
